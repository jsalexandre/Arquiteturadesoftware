package Model;

public class Fila {

	private int idFila;
	private String manutencaoSistemaERP;
	private String servidores;
	private String redes;
	private String telefonia;
	private String desktops;
	
	public int getIdFila() {
		return idFila;
	}
	public void setIdFila(int idFila) {
		this.idFila = idFila;
	}
	public String getManutencaoSistemaERP() {
		return manutencaoSistemaERP;
	}
	public void setManutencaoSistemaERP(String manutencaoSistemaERP) {
		this.manutencaoSistemaERP = manutencaoSistemaERP;
	}
	public String getServidores() {
		return servidores;
	}
	public void setServidores(String servidores) {
		this.servidores = servidores;
	}
	public String getRedes() {
		return redes;
	}
	public void setRedes(String redes) {
		this.redes = redes;
	}
	public String getTelefonia() {
		return telefonia;
	}
	public void setTelefonia(String telefonia) {
		this.telefonia = telefonia;
	}
	public String getDesktops() {
		return desktops;
	}
	public void setDesktops(String desktops) {
		this.desktops = desktops;
	}
	
	
}
